#!/usr/bin/env python3
"""Module pour la concaténation de chaînes de caractères.

Ce module fournit une fonction simple pour concaténer deux chaînes.
"""


def concat(str1: str, str2: str) -> str:
    """Concatène deux chaînes de caractères et retourne le résultat.
    
    Args:
        str1: Première chaîne à concaténer
        str2: Deuxième chaîne à concaténer
    
    Returns:
        La chaîne résultant de la concaténation de str1 et str2
    """
    return str1 + str2
